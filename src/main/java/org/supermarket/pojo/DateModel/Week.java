package org.supermarket.pojo.DateModel;

public class Week {
    private Double monday=0d;
    private Double tuesday=0d;
    private Double wednesday=0d;
    private Double thursday=0d;
    private Double friday=0d;
    private Double saturday=0d;
    private Double sunday=0d;


    public Double getMonday() {
        return monday;
    }

    public void setMonday(Double monday) {
        this.monday = monday;
    }

    public Double getTuesday() {
        return tuesday;
    }

    public void setTuesday(Double tuesday) {
        this.tuesday = tuesday;
    }

    public Double getWednesday() {
        return wednesday;
    }

    public void setWednesday(Double wednesday) {
        this.wednesday = wednesday;
    }

    public Double getThursday() {
        return thursday;
    }

    public void setThursday(Double thursday) {
        this.thursday = thursday;
    }

    public Double getFriday() { return friday; }

    public void setFriday(Double friday) { this.friday = friday; }

    public Double getSaturday() {
        return saturday;
    }

    public void setSaturday(Double saturday) {
        this.saturday = saturday;
    }

    public Double getSunday() {
        return sunday;
    }

    public void setSunday(Double sunday) {
        this.sunday = sunday;
    }
}
